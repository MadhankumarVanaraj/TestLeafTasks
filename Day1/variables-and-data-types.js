// Variables and Data types

// Number DataType
let number = 12
console.log(typeof(number)); //OUtput: number

//String Data Type 
let Product = "Quickly";
console.log(typeof(Product)); //Output: string

//Boolean Data Type
let Status = true;
console.log(typeof(Status)); //Output: boolean

//Big Int Data Type
let AccountNumber = 910035412568756n
console.log(typeof(AccountNumber));
console.log(""); //Output: bigint

//Undefined DataType
let AcNumber;
console.log(AcNumber);
console.log(typeof(AcNumber)); //Output: undefined 

//Null DataType
let Landlinenumber = null
console.log(Landlinenumber);
console.log(typeof(Landlinenumber)); //Output: Object 